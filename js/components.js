export function showToast(message, type = 'info') {
  let container = document.getElementById('toast-container');
  if (!container) {
    container = document.createElement('div');
    container.id = 'toast-container';
    container.className = 'toast-container';
    document.body.appendChild(container);
  }
  const toast = document.createElement('div');
  toast.className = `toast toast-${type}`;
  toast.innerHTML = `
    <span class="toast-message">${message}</span>
    <button class="toast-close" onclick="this.parentElement.remove()">✕</button>
  `;
  container.appendChild(toast);
  setTimeout(() => toast.remove(), 4000);
}

export function showModal(title, bodyHtml, footerHtml = '', opts = {}) {
  const existing = document.querySelector('.modal-backdrop');
  if (existing) existing.remove();
  const backdrop = document.createElement('div');
  backdrop.className = 'modal-backdrop';
  backdrop.innerHTML = `
    <div class="modal ${opts.large ? 'modal-lg' : ''}">
      <div class="modal-header">
        <h3 class="modal-title">${title}</h3>
        <button class="modal-close" id="modal-close-btn">✕</button>
      </div>
      <div class="modal-body">${bodyHtml}</div>
      ${footerHtml ? `<div class="modal-footer">${footerHtml}</div>` : ''}
    </div>
  `;
  document.body.appendChild(backdrop);
  backdrop.querySelector('#modal-close-btn').onclick = () => backdrop.remove();
  backdrop.addEventListener('click', e => { if (e.target === backdrop) backdrop.remove(); });
  return backdrop;
}

export function closeModal() {
  const m = document.querySelector('.modal-backdrop');
  if (m) m.remove();
}

export function confirm(title, message) {
  return new Promise(resolve => {
    const modal = showModal(title, `<p>${message}</p>`,
      `<button class="btn btn-secondary" id="confirm-cancel">Abbrechen</button>
       <button class="btn btn-primary" id="confirm-ok">Bestätigen</button>`
    );
    modal.querySelector('#confirm-cancel').onclick = () => { modal.remove(); resolve(false); };
    modal.querySelector('#confirm-ok').onclick = () => { modal.remove(); resolve(true); };
  });
}

export function renderProgressBar(percent, size = '') {
  const cls = percent >= 100 ? 'success' : percent >= 50 ? '' : 'warning';
  return `<div class="progress-bar ${size}"><div class="progress-fill ${cls}" style="width:${percent}%"></div></div>`;
}

export function renderBadge(text, type = 'neutral') {
  return `<span class="badge badge-${type}">${text}</span>`;
}

export function statusBadge(status) {
  const map = {
    'published': ['Veröffentlicht', 'success'],
    'draft': ['Entwurf', 'warning'],
    'incomplete': ['Unvollständig', 'error'],
    'review': ['In Prüfung', 'secondary'],
    'completed': ['Abgeschlossen', 'success'],
    'in_progress': ['In Bearbeitung', 'secondary'],
    'not_started': ['Nicht gestartet', 'neutral'],
    'passed': ['Bestanden', 'success'],
    'failed': ['Nicht bestanden', 'error'],
    'media_missing': ['Medien fehlen', 'warning']
  };
  const [label, type] = map[status] || [status, 'neutral'];
  return renderBadge(label, type);
}

